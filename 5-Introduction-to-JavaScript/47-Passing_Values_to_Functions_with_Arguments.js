// This lesson was about the parameters and arguments of functions. Parameters act as placeholder variables. When the function is called, the actual values that are passed in are the arguments.

// Example
function ourFunctionWithArgs(a, b) {
  console.log(a - b);
}
ourFunctionWithArgs(10, 5); // Outputs 5

// Only change code below this line.
function functionWithArgs(a, b) {
  console.log(a + b);
}

functionWithArgs(3, 4);
